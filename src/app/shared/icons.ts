export function getSvgIcon(iconName: string, size: number = 24, className: string = '', color: string = 'currentColor'): string {
  const icon = SVG_ICONS[iconName as keyof typeof SVG_ICONS];
  if (!icon) return '';
  
  return icon
    .replace('width="24"', `width="${size}"`)
    .replace('height="24"', `height="${size}"`)
    .replace('stroke="currentColor"', `stroke="${color}"`)
    .replace('<svg', `<svg class="${className}"`);
}

// Component helper for Angular templates
export function getIconHtml(iconName: string, size: number = 24, className: string = ''): string {
  return getSvgIcon(iconName, size, className);
}